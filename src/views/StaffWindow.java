    public void setTblOrders() {
        String[] headers = {"No. Orden", "No. Identificación", "Nombre Cliente", "Valor Total"};
        tblOrders.removeAll();
        
        DefaultTableModel tableModel = new DefaultTableModel();
        tableModel.setColumnIdentifiers(headers); 
        tblOrders.setModel(tableModel);
        
        initialData.getOrders().forEach((order) -> {
            tableModel.addRow(order.toArray());
        }); 
    }
    
