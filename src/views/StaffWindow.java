    private void tblOrdersMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblOrdersMouseClicked
        int selectedRow = tblOrders.getSelectedRow();
        OrderModel order = initialData.getOrders().get(selectedRow);
        
        lblOrderId.setText( "Orden No. " + String.valueOf(order.getId()) );
        lblCustomerId.setText(order.getCustomerId());
        lblCustomerName.setText(order.getCustomerName());
        lblOrderPrice.setText( "$ " + String.valueOf(order.getTotalPrice()) );
        
        String[] headers = {"Nombre", "Precio"};
        tblOrderItems.removeAll();

        DefaultTableModel tableModel = new DefaultTableModel();
        tableModel.setColumnIdentifiers(headers); 
        tblOrderItems.setModel(tableModel);

        order.getItems().forEach((item)->{
            tableModel.addRow(item.toArray());
        });
    }//GEN-LAST:event_tblOrdersMouseClicked

